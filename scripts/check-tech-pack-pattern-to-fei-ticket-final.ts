function checkPieceRowsAndInstances(): void {
  const modelSource = read('src/data/fcs/tech-packs.ts')
  const pageSource = read('src/pages/tech-pack/pattern-domain.ts')
  const eventsSource = read('src/pages/tech-pack/events.ts')
  ;[
    'colorPieceQuantities',
    'colorId',
    'colorName',
    'pieceQty',
    'enabled',
    'parsedQuantity',
    'totalPieceQty',
    'patternTotalPieceQty',
  ].forEach((field) => assertIncludes(modelSource + pageSource, field, `裁片明细颜色片数字段缺少 ${field}`))
  ;['适用颜色', '颜色片数', '当前部位总片数', '当前总片数', '解析参考片数'].forEach((text) =>
    assertIncludes(pageSource, text, `裁片明细页面缺少 ${text}`),
  )
  assert(!pageSource.includes('<th class="px-2 py-1 text-left">片数</th>'), '裁片明细主表仍有可编辑片数列')

  const pattern = getAllPatternItems().find((item) => item.pieceRows.length > 0 && item.pieceInstances.length > 0)
  assert(pattern, '缺少带裁片明细和裁片实例的纸样')
  const row = pattern!.pieceRows.find((item) => item.colorPieceQuantities.length > 0)
  assert(row, '缺少带颜色片数的裁片明细')
  assert(
    calculatePatternPieceTotalQty(row!.colorPieceQuantities) === row!.totalPieceQty,
    'totalPieceQty 与启用颜色 pieceQty 合计不一致',
  )
  assert(calculatePatternTotalPieceQty(pattern!.pieceRows) === pattern!.patternTotalPieceQty, 'patternTotalPieceQty 与行合计不一致')
  assert(row!.colorPieceQuantities.every((item) => Number.isInteger(item.pieceQty) && item.pieceQty >= 0), 'pieceQty 必须为非负整数')

  ;[
    'pieceInstances',
    'pieceInstanceId',
    'sourcePieceId',
    'sequenceNo',
    'displayName',
    'specialCraftAssignments',
    'craftCode',
    'craftName',
    'craftPosition',
    'craftPositionName',
  ].forEach((field) => assertIncludes(modelSource + pageSource, field, `裁片实例或逐片特殊工艺字段缺少 ${field}`))
  assert(PATTERN_CRAFT_POSITION_OPTIONS.map((item) => item.code).join(',') === 'LEFT,RIGHT,BOTTOM,FACE', '工艺位置 code 必须为 LEFT/RIGHT/BOTTOM/FACE')
  assert(PATTERN_CRAFT_POSITION_OPTIONS.map((item) => item.name).join(',') === '左,右,底,面', '工艺位置中文必须为 左/右/底/面')
  const generated = generatePieceInstancesFromColorQuantities({
    id: pattern!.id,
    pieceRows: pattern!.pieceRows,
    pieceInstances: pattern!.pieceInstances,
  })
  assert(generated.length === pattern!.patternTotalPieceQty, '裁片实例数量必须等于启用颜色 pieceQty 合计')
  const increasedRows = pattern!.pieceRows.map((item, index) => index === 0 ? ({
    ...item,
    colorPieceQuantities: item.colorPieceQuantities.map((qty, qtyIndex) => qtyIndex === 0 ? { ...qty, enabled: true, pieceQty: qty.pieceQty + 1 } : qty),
  }) : item)
  assert(generatePieceInstancesFromColorQuantities({ id: pattern!.id, pieceRows: increasedRows, pieceInstances: generated }).length > generated.length, '颜色片数增加未新增实例')
  const removed = findConfiguredPieceInstancesRemoved(
    [{ ...generated[0], specialCraftAssignments: pattern!.pieceInstances.find((item) => item.specialCraftAssignments.length > 0)?.specialCraftAssignments || [] }],
    [],
  )
  assert(removed.length >= 1, '颜色片数减少时未识别已配置实例')
  assertIncludes(pageSource + eventsSource, '当前减少片数会删除已配置特殊工艺的裁片实例', '删除已配置实例前缺少确认逻辑')

  const craftOptions = getPatternPieceInstanceSpecialCraftOptions()
  const optionNames = names(craftOptions)
  ;['绣花', '打条', '压褶', '打揽', '烫画', '直喷', '贝壳绣', '曲牙绣', '一字贝绣花', '模板工序', '激光开袋', '特种车缝（花样机）'].forEach((name) =>
    assert(optionNames.includes(name), `逐片特殊工艺选择器缺少 ${name}`),
  )
  ;['捆条', '橡筋定长切割', '缩水', '洗水'].forEach((name) =>
    assert(!optionNames.includes(name), `逐片特殊工艺选择器错误包含 ${name}`),
  )
  assertIncludes(pageSource + eventsSource, '该裁片已配置该特殊工艺，请勿重复添加', '缺少重复 craftCode 校验提示')
  assertIncludes(pageSource + eventsSource, '请选择工艺位置', '缺少未选工艺位置提示')
  assertIncludes(pageSource, '维护逐片工艺', '页面缺少维护逐片工艺入口')
  assertIncludes(pageSource, '应用到同颜色全部片', '页面缺少批量应用能力')
  const summary = summarizePieceInstances(pattern!.pieceInstances)
  assert(summary.pieceInstanceTotal === pattern!.pieceInstances.length, '裁片实例统计错误')
}

