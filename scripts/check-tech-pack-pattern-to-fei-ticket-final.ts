function checkCraftDictionary(): void {
  const prep = listPreparationProcesses()
  assert(prep.some((item) => item.processCode === 'PREP_SHRINKING' && item.processName === '缩水'), '准备阶段缺少 PREP_SHRINKING 缩水')
  assert(prep.some((item) => item.processCode === 'PREP_WASHING' && item.processName === '洗水'), '准备阶段缺少 PREP_WASHING 洗水')
  assert(!listSpecialCrafts().some((item) => item.craftName === '洗水'), '洗水仍存在于新特殊工艺清单')
  assert(listAuxiliaryCrafts().every((item) => item.craftCategoryName === '辅助工艺'), '辅助工艺类别不完整')
  assert(listSpecialTypeCrafts().every((item) => item.craftCategoryName === '特种工艺'), '特种工艺类别不完整')

  const auxiliaryNames = names(listAuxiliaryCrafts())
  ;['绣花', '打条', '压褶', '打揽', '烫画', '直喷', '贝壳绣', '曲牙绣', '一字贝绣花', '捆条'].forEach((name) =>
    assert(auxiliaryNames.includes(name), `辅助工艺缺少：${name}`),
  )
  const specialNames = names(listSpecialTypeCrafts())
  ;['模板工序', '激光开袋', '特种车缝（花样机）', '橡筋定长切割'].forEach((name) =>
    assert(specialNames.includes(name), `特种工艺缺少：${name}`),
  )

  assert(getCraftTargetObject('捆条') === 'FABRIC', '捆条适用对象必须为面料')
  assert(getCraftTargetObject('橡筋定长切割') === 'ACCESSORY', '橡筋定长切割适用对象必须为辅料')
  listSpecialCrafts()
    .filter((item) => !['捆条', '橡筋定长切割'].includes(item.craftName))
    .forEach((item) => assert(item.targetObject === 'CUT_PIECE_PART', `${item.craftName} 必须适用裁片部位`))
  ;['捆条', '橡筋定长切割', '缩水', '洗水'].forEach((name) =>
    assert(!canSelectCraftInPatternPiece(name), `${name} 不得进入裁片逐片特殊工艺选择器`),
  )
  const cutPieceCraftNames = names(listCutPiecePartCrafts())
  ;['捆条', '橡筋定长切割', '缩水', '洗水'].forEach((name) =>
    assert(!cutPieceCraftNames.includes(name), `${name} 不得出现在 listCutPiecePartCrafts`),
  )
  const cuttingCraftNames = names(listCuttingCrafts())
  ;['普通裁', '激光定位裁', '定向裁'].forEach((name) =>
    assert(cuttingCraftNames.includes(name), `裁床工序缺少：${name}`),
  )
  assert(listFabricCrafts().some((item) => item.craftName === '捆条'), '面料级工艺缺少捆条')
  assert(listAccessoryCrafts().some((item) => item.craftName === '橡筋定长切割'), '辅料级工艺缺少橡筋定长切割')
}

function checkBomLinkage(): void {
  const techPack = getPrimaryTechPack()
  const bomSource = read('src/pages/tech-pack/bom-domain.ts')
  const bomLinkageSource = read('src/pages/tech-pack/bom-process-linkage.ts')
  const techPackSource = read('src/data/fcs/tech-packs.ts')
  assertIncludes(techPackSource, 'shrinkRequirement', 'BomItemRow 或技术包 BOM 模型缺少 shrinkRequirement')
  assertIncludes(techPackSource, 'washRequirement', 'BomItemRow 或技术包 BOM 模型缺少 washRequirement')
  assertIncludes(bomSource, '缩水需求', '物料清单页面缺少缩水需求列')
  assertIncludes(bomSource, '洗水需求', '物料清单页面缺少洗水需求列')
  assertIncludes(bomLinkageSource, "'否'", '缩水/洗水需求默认值或选项缺少“否”')

  const shrinkRows = [{ ...techPack.bomItems[0], shrinkRequirement: '是', washRequirement: '否' }]
  const washRows = [{ ...techPack.bomItems[0], shrinkRequirement: '否', washRequirement: '是' }]
  assert(shouldGenerateShrinkProcess(shrinkRows), '任一 BOM 行缩水需求为是时未触发缩水')
  assert(shouldGenerateWashProcess(washRows), '任一 BOM 行洗水需求为是时未触发洗水')
  assert(!shouldGenerateShrinkProcess([{ ...techPack.bomItems[0], shrinkRequirement: '否' }]), '全部缩水需求为否时仍触发缩水')
  assert(!shouldGenerateWashProcess([{ ...techPack.bomItems[0], washRequirement: '否' }]), '全部洗水需求为否时仍触发洗水')

  const shrink = buildAutoGeneratedShrinkProcess()
  const wash = buildAutoGeneratedWashProcess()
  assert(shrink.sourceType === 'BOM' && shrink.triggerField === 'shrinkRequirement', '自动生成缩水来源或触发字段错误')
  assert(wash.sourceType === 'BOM' && wash.triggerField === 'washRequirement', '自动生成洗水来源或触发字段错误')

  const linked = syncPreparationProcessesFromBom([], [
      { ...techPack.bomItems[0], shrinkRequirement: '是', washRequirement: '是' },
      ...techPack.bomItems.slice(1),
    ])
  assert(linked.techniques.some((item) => item.processCode === 'PREP_SHRINKING'), 'BOM 联动未生成 PREP_SHRINKING')
  assert(linked.techniques.some((item) => item.processCode === 'PREP_WASHING'), 'BOM 联动未生成 PREP_WASHING')
  const manualRemoval = syncPreparationProcessesFromBom(
    [{ ...shrink, hasManualOverride: true, manualNotes: '人工备注' }],
    [{ ...techPack.bomItems[0], shrinkRequirement: '否', washRequirement: '否' }],
  )
  assert(
    manualRemoval.techniques.some((item) => item.processCode === 'PREP_SHRINKING' && item.requiresRemovalConfirmation),
    '有人工维护内容的缩水不应静默删除',
  )

  const snapshotSources = [
    read('src/data/pcs-technical-data-version-types.ts'),
    read('src/data/pcs-technical-data-fcs-adapter.ts'),
  ].join('\n')
  ;['shrinkRequirement', 'washRequirement'].forEach((field) =>
    assertIncludes(snapshotSources, field, `技术包版本快照缺少 ${field}`),
  )
  const linkedTechniques = buildTechniquesFromTechPack(techPack, buildBomItemsFromTechPack(techPack))
  assert(
    linkedTechniques.some((item) => item.processCode === 'PREP_SHRINKING' || item.processCode === 'PREP_WASHING'),
    '技术包快照派生工序缺少 PREP_SHRINKING/PREP_WASHING',
  )
}

