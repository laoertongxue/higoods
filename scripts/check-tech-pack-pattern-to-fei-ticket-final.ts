function file(relativePath: string): string {
  return path.join(ROOT, relativePath)
}

function read(relativePath: string): string {
  return readFileSync(file(relativePath), 'utf8')
}

function assert(condition: unknown, message: string): void {
  if (!condition) throw new Error(message)
}

function assertIncludes(source: string, value: string, message: string): void {
  assert(source.includes(value), message)
}

function names<T extends { craftName?: string; processName?: string }>(items: T[]): string[] {
  return items.map((item) => item.craftName || item.processName || '')
}

function runPreviousScripts(): void {
  previousScripts.forEach((script) => {
    assert(existsSync(file(script)), `缺少前置检查脚本：${script}`)
    const result = spawnSync('npx', ['tsx', script], {
      cwd: ROOT,
      encoding: 'utf8',
      stdio: 'pipe',
    })
    assert(
      result.status === 0,
      `前置检查脚本执行失败：${script}\n${result.stdout}\n${result.stderr}`,
    )
  })
}

